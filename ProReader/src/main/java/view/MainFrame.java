    private void exit_to_LibraryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_exit_to_LibraryMouseClicked
        int result = JOptionPane.showConfirmDialog(null, "Bạn có muốn lưu thay đổi trước khi thoát?", "Trở về Library?", JOptionPane.YES_NO_CANCEL_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            // Lưu thay đổi
            save_Change(1);
        } else if (result == JOptionPane.NO_OPTION) {
            save_Change(0);
            //System.exit(0);
        } else {
            // Hủy bỏ việc thoát
            evt.consume();
        }
    }//GEN-LAST:event_exit_to_LibraryMouseClicked
    
    private void save_Change(int i){
        //1. Cập nhật vào recent List
        if(i == 1){
            this.libraryFrame.dataHandler.check_addNewBook_in_RecentList(this.data.book);
            this.libraryFrame.add_RecentList_to_table();
        }
        //2. Tắt MainFrame, bật Library_Frame.
        this.libraryFrame.open();
        this.setVisible(false);
    }
    
