    public int countPage;
    public int currentPage;
    public PDFRenderer render;
    public BufferedImage image1, image2;
    private void initData(){
        this.countPage = this.data.document.getNumberOfPages();
        this.currentPage = 0;
        this.render = new PDFRenderer(this.data.document);
    }
    
    public void drawPage(){
        status_page.setText((this.currentPage + 1) + ", " + (this.currentPage + 2) + " / " + this.countPage);
        try {
            this.image1 = render.renderImageWithDPI(this.currentPage,55);
            this.image2 = render.renderImageWithDPI(this.currentPage + 1, 55);
            //BufferedImage scaledImage1 = (BufferedImage) image1.getScaledInstance(480, 586, Image.SCALE_DEFAULT);
            //BufferedImage scaledImage2 = (BufferedImage) image2.getScaledInstance(480, 586, Image.SCALE_DEFAULT);
            ImageIcon imageIcon1 = new ImageIcon(image1);
            ImageIcon imageIcon2 = new ImageIcon(image2);
            
            ImageIcon imageIcon1_resize1 = new ImageIcon(imageIcon1.getImage().getScaledInstance(50, 50,1));
            ImageIcon imageIcon1_resize2= new ImageIcon(imageIcon2.getImage().getScaledInstance(50, 50,1));

            this.page_1.setIcon(new ImageIcon(image1));
            this.page_2.setIcon(new ImageIcon(image2));
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
    
// 5. Chuyển trang:
    private void DownPage(){
        if(currentPage > 0){
            currentPage -= 2;
            drawPage();
        }
        
    }
    public void UpPage(){
        if(currentPage < countPage - 2){
            currentPage += 2;
            drawPage();
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
